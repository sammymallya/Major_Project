"""
Public interface for the F1 Score component.

Computes F1 score using ROUGE metrics for QA evaluation.
"""

from __future__ import annotations

import logging

from rouge_score import rouge_scorer  # type: ignore[import]

from .config import F1ScoreSettings, get_f1_score_settings
from .types import F1ScoreResult

logger = logging.getLogger(__name__)


def compute_f1_score(expected_answer: str, actual_answer: str) -> F1ScoreResult:
    """
    Compute F1 score using ROUGE between expected and actual answers.

    Args:
        expected_answer: The ground truth answer.
        actual_answer: The answer generated by the pipeline.

    Returns:
        F1ScoreResult with F1, precision, recall, and rouge type.
    """
    settings = get_f1_score_settings()

    logger.debug("Computing F1 score using %s", settings.rouge_type)

    scorer = rouge_scorer.RougeScorer([settings.rouge_type], use_stemmer=settings.use_stemmer)
    scores = scorer.score(expected_answer, actual_answer)

    rouge_scores = scores[settings.rouge_type]
    f1 = rouge_scores.fmeasure
    precision = rouge_scores.precision
    recall = rouge_scores.recall

    logger.debug("F1: %.4f, Precision: %.4f, Recall: %.4f", f1, precision, recall)

    return F1ScoreResult(
        f1=f1,
        precision=precision,
        recall=recall,
        rouge_type=settings.rouge_type,
    )