"""
Public interface for the Semantic Matching component.

Computes semantic similarity between expected and actual answers.
"""

from __future__ import annotations

import logging

from sentence_transformers import SentenceTransformer, util  # type: ignore[import]

from .config import SemanticMatchingSettings, get_semantic_matching_settings
from .types import SemanticMatchResult

logger = logging.getLogger(__name__)

# Global model (lazy load)
_model: SentenceTransformer | None = None


def _load_model(settings: SemanticMatchingSettings) -> None:
    """Lazy load the sentence transformer model."""
    global _model
    if _model is None:
        logger.info("Loading sentence-transformers model '%s' for semantic matching", settings.embedding_model_name)
        _model = SentenceTransformer(settings.embedding_model_name)


def compute_semantic_similarity(expected_answer: str, actual_answer: str) -> SemanticMatchResult:
    """
    Compute semantic similarity between expected and actual answers.

    Args:
        expected_answer: The ground truth answer.
        actual_answer: The answer generated by the pipeline.

    Returns:
        SemanticMatchResult with similarity score and model name.
    """
    settings = get_semantic_matching_settings()
    _load_model(settings)

    logger.debug("Computing embeddings for semantic similarity")

    # Embed both texts
    embeddings = _model.encode([expected_answer, actual_answer], convert_to_tensor=True)
    expected_emb, actual_emb = embeddings[0], embeddings[1]

    # Compute cosine similarity
    score = util.cos_sim(expected_emb, actual_emb).item()

    logger.debug("Semantic similarity score: %.4f", score)

    return SemanticMatchResult(score=score, model_name=settings.embedding_model_name)